package vinicius28836.MODEL;

public class Point {
	
	private float x;
	private float y;
	//****************************************************************************************
	public Point(float x, float y){
		this.x = x;
		this.y = y;
	}
	
	//****************************************************************************************
	
	public float x(){
		return x;
	}
	
	//****************************************************************************************
	
	public float y(){
		return y;
	}
	
	//****************************************************************************************
}
